// ─────────────────────────────────────────────────────────────────────────────
// Selection: rotate by day-of-year so each calendar day maps to a stable
// entry. The catalog is grouped by theme above for readability, but a
// straight modulo would cluster consecutive days within a single theme.
// Multiplying day-of-year by a number coprime with the catalog size (47
// here, coprime with 120) spreads the rotation across themes so each day
// feels distinct from the one before. The full cycle still visits all 120
// entries before repeating.
// ─────────────────────────────────────────────────────────────────────────────

const JUMP = 47;

/**
 * Return the devotional for today (or any given date), using the date's
 * day-of-year (multiplied by a coprime jump) to select an entry deterministically.
 */
export function getTodaysDevotional(date = new Date()) {
  const start = new Date(date.getFullYear(), 0, 0);
  const diff = date - start;
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));
  const index = (dayOfYear * JUMP) % DEVOTIONALS.length;
  return DEVOTIONALS[index];
}

